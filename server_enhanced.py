# Possible attacks ideas:


# Possible attack number 1


# If two people try to sign in at the same time, there's a problem.
# It could lead to unauthorized access or other bad things because the system might get confused.
# What if 2 people sign in at the same time for user 1, would that mean user 1 would be assigned to both?

# To fix this, we should make sure that only one person can sign in one at a time.



# Possible attack number 2

#The encryptions can appear similar each time a message is sent,
# which may alert attackers to a pattern in our communication.
# For instance, if the message 'asdfasnfa' is consistently encrypted, it could be a potential risk.
# To mitigate this, it is crucial to randomize the encryption for each transmission, adding an extra layer of protection.

